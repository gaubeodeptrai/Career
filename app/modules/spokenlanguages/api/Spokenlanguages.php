<?php
namespace app\modules\spokenlanguages\api;

use Yii;
use yii\data\ActiveDataProvider;
use yii\easyii\models\Tag;
use yii\easyii\widgets\Fancybox;
use yii\widgets\LinkPager;

use app\modules\spokenlanguages\models\Spokenlanguages as SpokenlanguagesModel;

/**
 * Spokenlanguages module API
 * @package app\modules\spokenlanguages\api
 *
 * @method static SpokenlanguagesObject get(mixed $id_slug) Get spokenlanguages object by id or slug
 * @method static array items(array $options = []) Get list of spokenlanguages as SpokenlanguagesObject objects
 * @method static mixed last(int $limit = 1) Get last spokenlanguages
 * @method static void plugin() Applies FancyBox widget on photos called by box() function
 * @method static string pages() returns pagination html generated by yii\widgets\LinkPager widget.
 * @method static \stdClass pagination() returns yii\data\Pagination object.
 */

class Spokenlanguages extends \yii\easyii\components\API
{
    private $_adp;
    private $_last;
    private $_items;
    private $_item = [];

    public function api_items($options = [])
    {
        if(!$this->_items){
            $this->_items = [];

            $with = ['seo'];
            if(Yii::$app->getModule('admin')->activeModules['spokenlanguages']->settings['enableTags']){
                $with[] = 'tags';
            }
            $query = SpokenlanguagesModel::find()->with($with)->status(SpokenlanguagesModel::STATUS_ON);

            if(!empty($options['where'])){
                $query->andFilterWhere($options['where']);
            }
            if(!empty($options['tags'])){
                $query
                    ->innerJoinWith('tags', false)
                    ->andWhere([Tag::tableName() . '.name' => (new SpokenlanguagesModel)->filterTagValues($options['tags'])])
                    ->addGroupBy('spokenlanguages_id');
            }
            if(!empty($options['orderBy'])){
                $query->orderBy($options['orderBy']);
            } else {
                $query->sortDate();
            }

            $this->_adp = new ActiveDataProvider([
                'query' => $query,
                'pagination' => !empty($options['pagination']) ? $options['pagination'] : []
            ]);

            foreach($this->_adp->models as $model){
                $this->_items[] = new SpokenlanguagesObject($model);
            }
        }
        return $this->_items;
    }

    public function api_get($id_slug)
    {
        if(!isset($this->_item[$id_slug])) {
            $this->_item[$id_slug] = $this->findSpokenlanguages($id_slug);
        }
        return $this->_item[$id_slug];
    }

    public function api_last($limit = 1)
    {
        if($limit === 1 && $this->_last){
            return $this->_last;
        }

        $with = ['seo'];
        if(Yii::$app->getModule('admin')->activeModules['spokenlanguages']->settings['enableTags']){
            $with[] = 'tags';
        }

        $result = [];
        foreach(SpokenlanguagesModel::find()->with($with)->status(SpokenlanguagesModel::STATUS_ON)->sortDate()->limit($limit)->all() as $item){
            $result[] = new SpokenlanguagesObject($item);
        }

        if($limit > 1){
            return $result;
        } else {
            $this->_last = count($result) ? $result[0] : null;
            return $this->_last;
        }
    }

    public function api_plugin($options = [])
    {
        Fancybox::widget([
            'selector' => '.easyii-box',
            'options' => $options
        ]);
    }

    public function api_pagination()
    {
        return $this->_adp ? $this->_adp->pagination : null;
    }

    public function api_pages()
    {
        return $this->_adp ? LinkPager::widget(['pagination' => $this->_adp->pagination]) : '';
    }

    private function findSpokenlanguages($id_slug)
    {
        $spokenlanguages = SpokenlanguagesModel::find()->where(['or', 'language_id=:id_slug', 'slug=:id_slug'], [':id_slug' => $id_slug])->status(SpokenlanguagesModel::STATUS_ON)->one();
        if($spokenlanguages) {
            $spokenlanguages->updateCounters(['views' => 1]);
            return new SpokenlanguagesObject($spokenlanguages);
        } else {
            return null;
        }
    }
}